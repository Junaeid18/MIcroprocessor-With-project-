A3 SEGMENT PARA PUBLIC 'CODE'
ASSUME CS:A3
ORG 1000H

START:

;control register turn on
MOV AL,80H
OUT 1FH,AL

MOV SI,OFFSET DATA

;LED turn on

;counter
MOV BL,03H

L1:

MOV AL,BYTE PTR CS:[SI]
OUT 19H,AL

;for delay
MOV CX,0FFFFH
L2:LOOP L2

INC SI
DEC BL
CMP BL,00H

JA L1

DATA:
DB 092H
DB 08EH
DB 099H

A3 ENDS

END START